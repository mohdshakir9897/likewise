//program for loop use
 #include <iostream>
 using namespace std;
 // start of main function

 int main()
 {
     int i;

     //for loop usage
     for(i=1;i<10;i++)
     {
         cout<<endl<<i<<endl;

     }//end of loop

     //while loop usage
    
     i=1;
     while(i<10)

     {
         cout<<endl<<i;

         i++;
 }//end of while loop
  

 //do while loop
i=1;
 do {
     cout<<endl<<i;

     i++;

 }while(i<10);//end of do while loop

 return 0;

 }//end of main function